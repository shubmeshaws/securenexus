function buildSecretsUrlCell(urls: string[] | undefined): string {
  if (!urls?.length) return '—';

  const items = urls
    .map(
      (url) =>
        `<li><a href="${escapeHtmlAttr(url)}" target="_blank" rel="noopener noreferrer">${escapeHtml(url)}</a></li>`
    )
    .join('');

  return `<ul class="source-url-list">${items}</ul>`;
}

function buildSecretsRemediationCell(row: SecretsFindingRow): string {
  const remediation: SecretsRemediation = row.recommendation
    ? {
        summary: row.recommendation,
        steps: row.remediationSteps ?? [],
        commands: row.remediationCommands ?? [],
      }
    : resolveSecretsRemediation(row.rule, row.location);

  const steps = remediation.steps
    .map((step) => `<li>${escapeHtml(step)}</li>`)
    .join('');
  const commands = remediation.commands.length
    ? `<pre class="remediation-cmd">${remediation.commands.map((cmd) => escapeHtml(cmd)).join('\n')}</pre>`
    : '';

  return `<div class="remediation">
    <p class="remediation-summary">${escapeHtml(remediation.summary)}</p>
    ${steps ? `<ol class="remediation-steps">${steps}</ol>` : ''}
    ${commands}
  </div>`;
}

function buildSecretsObservations(repoName: string, findings: SecretsFindingRow[]): string[] {
  if (!findings.length) return ['No hard-coded secrets detected in this scan.'];

  const observations: string[] = [];
  const high = findings.filter((f) => /high|critical/i.test(f.severity));
  const keys = findings.filter((f) => /private-key|ssh|rsa/i.test(`${f.rule}${f.message}`));
  const cloud = findings.filter((f) => /aws|azure|gcp|github|gitlab|slack|stripe/i.test(`${f.rule}${f.message}`));

  if (high.length) {
    observations.push(
      `${high.length} high-severity secret${high.length === 1 ? '' : 's'} in ${repoName} require immediate rotation and removal from source control.`
    );
  }
  if (cloud.length) {
    observations.push(
      `Cloud or SaaS credentials detected (${cloud.length} finding${cloud.length === 1 ? '' : 's'}) — revoke and rotate affected tokens.`
    );
  }
  if (keys.length) {
    observations.push(
      `Private key material found in ${keys.length} location${keys.length === 1 ? '' : 's'}.`
    );
  }
  observations.push(
    `Review ${findings.length} finding${findings.length === 1 ? '' : 's'} in the secrets summary table below.`
  );
  return observations.slice(0, 6);
}

