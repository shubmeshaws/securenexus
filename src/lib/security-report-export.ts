export function sampleFindings(toolId: string): Array<{
  severity: string;
  rule: string;
  location: string;
  message: string;
}> {
  const base = [
    {
      severity: 'High',
      rule: `${toolId}:hardcoded-secret`,
      location: 'src/config/settings.ts:42',
      message: 'Potential hard-coded credential detected in configuration module.',
    },
    {
      severity: 'Medium',
      rule: `${toolId}:outdated-dependency`,
      location: 'package-lock.json',
      message: 'Transitive dependency has a known CVE with a patched version available.',
    },
    {
      severity: 'Low',
      rule: `${toolId}:missing-security-header`,
      location: 'nginx.conf',
      message: 'Content-Security-Policy header is not configured.',
    },
  ];

  if (toolId === 'zap') {
    return [
      {
        severity: 'High',
        rule: 'zap:10021',
        location: '/api/login',
        message: 'X-Content-Type-Options header missing on authentication endpoint.',
      },
      {
        severity: 'Medium',
        rule: 'zap:90022',
        location: '/dashboard',
        message: 'Application does not set anti-clickjacking headers.',
      },
    ];
  }

  return base;
}

export function countFindingsBySeverity(findings: Array<{ severity: string }>): {
  high: number;
  medium: number;
  low: number;
} {
  let high = 0;
  let medium = 0;
  let low = 0;
  for (const row of findings) {
    const sev = row.severity.toLowerCase();
    if (sev === 'high' || sev === 'critical') high += 1;
    else if (sev === 'medium' || sev === 'moderate') medium += 1;
    else low += 1;
  }
  return { high, medium, low };
}

export function countScaDependenciesBySeverity(toolId: string): {
  high: number;
  medium: number;
  low: number;
} {
  return countFindingsBySeverity(sampleScaDependencies(toolId));
}

export function securityReportToPdfBuffer(ctx: SecurityReportContext): Promise<Buffer> {
  const findings =
    ctx.severityCounts && ctx.tool.id === 'semgrep'
      ? [
          {
            severity: 'Summary',
            rule: 'Semgrep live scan',
            location: '—',
            message: `${ctx.severityCounts.high} high · ${ctx.severityCounts.medium} medium · ${ctx.severityCounts.low} low findings. Open the HTML report for full details.`,
          },
        ]
      : sampleFindings(ctx.tool.id);
  return import('pdfkit').then(
    ({ default: PDFDocument }) =>
      new Promise((resolve, reject) => {
        const doc = new PDFDocument({ margin: 48, size: 'A4' });
        const chunks: Buffer[] = [];

        doc.on('data', (chunk: Buffer) => chunks.push(chunk));
        doc.on('end', () => resolve(Buffer.concat(chunks)));
        doc.on('error', reject);

        doc.fontSize(18).fillColor('#0f172a').text(ctx.title);
        doc.moveDown(0.4);
        doc.fontSize(10).fillColor('#64748b');
        doc.text(`Tool: ${ctx.tool.name}`);
        doc.text(`Generated: ${formatTimestampIST(new Date().toISOString())}`);
        doc.text(ctx.summary);
        doc.moveDown(0.8);

        doc.fontSize(11).fillColor('#0f172a').text('Findings', { underline: true });
        doc.moveDown(0.4);

        findings.forEach((row, index) => {
          doc.fontSize(9).fillColor('#334155');
          doc.text(`${index + 1}. [${row.severity}] ${row.rule}`);
          doc.fontSize(8).fillColor('#64748b');
          doc.text(`   ${row.location} — ${row.message}`);
          doc.moveDown(0.35);
        });

        doc.moveDown(0.5);
        doc.fontSize(8).fillColor('#94a3b8').text('SecureNexus Security Report');
        doc.end();
      })
  );
}
