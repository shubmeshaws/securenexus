function scaSeverityClass(severity: string): string {
  const sev = severity.toLowerCase();
  if (sev === 'high' || sev === 'critical') return 'sev-sca-high';
  if (sev === 'moderate' || sev === 'medium') return 'sev-sca-moderate';
  return 'sev-sca-low';
}

