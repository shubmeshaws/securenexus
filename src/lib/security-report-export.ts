export interface SecurityReportContext {
  resource: SecurityResourceView | null;
  tool: SecurityToolDefinition;
  title: string;
  summary: string;
  severityCounts?: { high: number; medium: number; low: number };
  scaDependencies?: ScaDependencyRow[];
  secretsFindings?: SecretsFindingRow[];
}

export interface SecretsFindingRow {
  id?: string;
  severity: string;
  rule: string;
  location: string;
  message: string;
  urls?: string[];
  recommendation?: string;
  remediationSteps?: string[];
  remediationCommands?: string[];
}

export function buildSecurityReportHtml(ctx: SecurityReportContext): string {
  if (ctx.tool.category === 'sast') {
    return buildSastReportHtml(ctx);
  }
  if (ctx.tool.category === 'sca') {
    return buildScaReportHtml(ctx);
  }
  if (ctx.tool.category === 'secrets') {
    return buildSecretsReportHtml(ctx);
  }

  const resourceName = ctx.resource?.name ?? 'Target';
  const target =
    ctx.resource?.type === 'repository'
      ? `${ctx.resource.repoUrl ?? '—'}${ctx.resource.defaultBranch ? ` (${ctx.resource.defaultBranch})` : ''}`
      : ctx.resource?.targetUrl ?? '—';
  const generatedAt = formatTimestampIST(new Date().toISOString());
  const docTitle = buildReportDocumentTitle(ctx.tool.category, ctx.tool.name, resourceName);
  const targetLabel = ctx.resource?.type === 'target_url' ? 'URL Target' : 'Repository';

  const findings = sampleFindings(ctx.tool.id);

  const rows = findings
    .map(
      (row) => `
      <tr>
        <td>${escapeHtml(row.severity)}</td>
        <td>${escapeHtml(row.rule)}</td>
        <td><code>${escapeHtml(row.location)}</code></td>
        <td>${escapeHtml(row.message)}</td>
      </tr>`
    )
    .join('');

  const header = buildReportHeaderHtml({
    categoryLabel: categoryReportLabel(ctx.tool.category),
    toolName: escapeHtml(ctx.tool.name),
    resourceName: escapeHtml(resourceName),
    subtitle: categoryReportSubtitle(ctx.tool.category),
    generatedAt: escapeHtml(generatedAt),
    targetLabel,
    targetValue: escapeHtml(target),
  });

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1.0" />
  <title>${escapeHtml(docTitle)}</title>
  <style>${reportPageStyles()}</style>
</head>
<body>
  <div class="page">
    ${header}
    <div class="report-body">
      <div class="summary-box">${escapeHtml(ctx.summary)}</div>
      <h2>Findings</h2>
      <table class="findings-table">
        <thead>
          <tr>
            <th>Severity</th>
            <th>Rule</th>
            <th>Location</th>
            <th>Description</th>
          </tr>
        </thead>
        <tbody>${rows}</tbody>
      </table>
    </div>
    <p class="report-footer">SecureNexus Security · Generated by ${escapeHtml(ctx.tool.name)}</p>
  </div>
</body>
</html>`;
}

function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function escapeHtmlAttr(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;');
}

function parseLocationParts(location: string): { file: string; line?: number } {
  const colon = location.lastIndexOf(':');
  if (colon <= 0) return { file: location };
  const tail = location.slice(colon + 1);
  if (!/^\d+$/.test(tail)) return { file: location };
  return { file: location.slice(0, colon), line: Number(tail) };
}

function enrichSecretsFindingUrls(
  findings: SecretsFindingRow[],
  resource: SecurityResourceView | null
): SecretsFindingRow[] {
  if (!resource?.repoUrl?.trim()) return findings;

  const context: RepoSourceContext = {
    repoUrl: resource.repoUrl,
    defaultBranch: resource.defaultBranch,
  };

  return findings.map((row) => {
    if (row.urls?.length) return row;
    const { file, line } = parseLocationParts(row.location);
    const urls = buildFindingSourceUrls(context, {
      file,
      startLine: line,
      endLine: line,
    });
    return urls.length ? { ...row, urls } : row;
  });
}

interface SastFindingRow {
  severity: string;
  rule: string;
  location: string;
  message: string;
}

function bucketFinding(finding: SastFindingRow): 'Critical' | 'High' | 'Medium' | 'Low' | 'Warning' {
  const sev = finding.severity.toLowerCase();
  if (sev === 'critical') return 'Critical';
  if (sev === 'high') return 'High';
  if (sev === 'medium') return 'Medium';
  if (sev === 'warning') return 'Warning';
  return 'Low';
}

function buildSastObservations(repoName: string, findings: SastFindingRow[]): string[] {
  if (!findings.length) return ['No SAST findings detected in this scan.'];

  const observations: string[] = [];
  const sql = findings.filter((f) => /sql/i.test(f.rule) || /sql/i.test(f.message));
  const secrets = findings.filter((f) =>
    /secret|credential|password|api-key/i.test(`${f.rule}${f.message}`)
  );
  const high = findings.filter((f) => /high|critical/i.test(f.severity));

  if (sql.length) {
    observations.push(
      `SQL injection or unsafe query patterns found in ${repoName} (${sql.length} finding${sql.length === 1 ? '' : 's'}).`
    );
  }
  if (secrets.length) {
    observations.push(
      `Hardcoded secrets or credential patterns detected in ${repoName} (${secrets.length} finding${secrets.length === 1 ? '' : 's'}).`
    );
  }
  if (high.length) {
    observations.push(
      `${high.length} high-severity issue${high.length === 1 ? '' : 's'} in ${repoName} require immediate remediation.`
    );
  }
  if (observations.length < 3) {
    observations.push(
      `Review ${findings.length} finding${findings.length === 1 ? '' : 's'} in the detailed SAST summary table below.`
    );
  }
  return observations.slice(0, 6);
}

