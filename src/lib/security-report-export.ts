function countScaBuckets(deps: ScaDependencyRow[]): {
  Critical: number;
  High: number;
  Medium: number;
  Low: number;
  Warning: number;
} {
  const counts = { Critical: 0, High: 0, Medium: 0, Low: 0, Warning: 0 };
  for (const row of deps) {
    if (row.severity === 'High') counts.High += 1;
    else if (row.severity === 'Moderate') counts.Medium += 1;
    else counts.Low += 1;
  }
  return counts;
}

function buildScaObservations(repoName: string, deps: ScaDependencyRow[]): string[] {
  if (!deps.length) return ['No vulnerable dependencies detected in this scan.'];

  const observations: string[] = [];
  const high = deps.filter((d) => d.severity === 'High');
  const moderate = deps.filter((d) => d.severity === 'Moderate');
  const packages = new Set(deps.map((d) => d.package));

  if (high.length) {
    observations.push(
      `${high.length} high-severity vulnerable package${high.length === 1 ? '' : 's'} in ${repoName} require immediate upgrade or patching.`
    );
  }
  if (moderate.length) {
    observations.push(
      `${moderate.length} moderate-severity dependencies flagged with known CVEs — schedule remediation in the next sprint.`
    );
  }
  observations.push(
    `${deps.length} outdated or vulnerable dependencies identified across ${packages.size} package${packages.size === 1 ? '' : 's'}.`
  );

  const redos = deps.filter((d) => /redos/i.test(d.vulnerability));
  if (redos.length) {
    observations.push(
      `ReDoS (Regular Expression Denial of Service) vulnerabilities found in ${redos.length} package${redos.length === 1 ? '' : 's'}.`
    );
  }

  const pollution = deps.filter((d) => /prototype pollution/i.test(d.vulnerability));
  if (pollution.length) {
    observations.push('Prototype pollution risk identified — review object merge patterns in dependent code.');
  }

  return observations.slice(0, 6);
}

